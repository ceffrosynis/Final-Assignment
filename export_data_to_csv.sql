SELECT 'invoice_and_item_number',
  'date',
  'store_number',
  'store_name',
  'address',
  'city',
  'zip_code',
  'store_location',
  'county_number',
  'county',
  'category',
  'category_name',
  'vendor_number',
  'vendor_name',
  'item_number',
  'item_description',
  'pack',
  'bottle_volume_ml',
  'state_bottle_cost',
  'state_bottle_retail',
  'bottles_sold',
  'sale_dollars',
  'volume_sold_liters',
  'volume_sold_gallons' 
   UNION ALL SELECT invoice_and_item_number,
   date,
   store_number,
   store_name,
   address,
   city,
   zip_code,
   store_location,
   county_number,
   county,
   category,
   category_name,
   vendor_number,
   vendor_name,
   item_number,
   item_description,
   pack,
   bottle_volume_ml,
   state_bottle_cost,
   state_bottle_retail,
   bottles_sold,
   sale_dollars,
   volume_sold_liters,
   volume_sold_gallons
       FROM finance_liquor_sales
       WHERE date >= '2016/01/01' and date < '2020/01/01'
       INTO OUTFILE '/var/lib/mysql-files/filename.csv'
       FIELDS TERMINATED BY ','
       ENCLOSED BY '"'
       LINES TERMINATED BY '\n';